import type { AppState, User } from '../types'

const DAY = 24 * 60 * 60 * 1000

function mkUser(
  id: string,
  createdDaysAgo: number,
  profile: User['profile'],
): User {
  return {
    id,
    createdAt: Date.now() - createdDaysAgo * DAY,
    acceptedSafety: true,
    onboardingComplete: true,
    endedRelationshipCount: 0,
    profile,
  }
}

